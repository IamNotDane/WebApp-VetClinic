    public function edit($id)
    {
        $clients = Clients::where('email', Request::get('email'))->first();
        if ($clients === null) {
            return redirect('/');
        }
        else {
            return view('pages.regtwo')->with('clients', $clients);
        }
    }

    /**
     * Update the specified resource in storage.
